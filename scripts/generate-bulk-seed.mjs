// Generates bulk filler data (resources -> physical_locations -> resource_hours)
// using @faker-js/faker and writes it into supabase/seed.sql between markers.
// Re-run any time to regenerate; it replaces only its own marked block, leaving
// the hand-crafted edge-case seed data above it untouched.
//
// Deterministic: a fixed faker seed + a fixed reference "now" date mean every
// run produces byte-identical output. Bump SEED to intentionally get a new set.
//
// Usage: node scripts/generate-bulk-seed.mjs

import { faker } from "@faker-js/faker";
import { readFileSync, writeFileSync } from "node:fs";

const SEED = 20260630;
const REFERENCE_DATE = new Date("2026-06-30T00:00:00Z");
faker.seed(SEED);

const SEED_PATH = new URL("../supabase/seed.sql", import.meta.url);
const BEGIN_MARKER = "-- BEGIN FAKER BULK SEED (generated by scripts/generate-bulk-seed.mjs)";
const END_MARKER = "-- END FAKER BULK SEED";

const RESOURCE_COUNT = 300;
const LOCATION_COUNT = 400;

// Reuse the users already created in the hand-crafted part of seed.sql.
const USER_IDS = [
  "00000000-0000-4000-8000-000000000001", // admin
  "00000000-0000-4000-8000-000000000002", // admin2
  "00000000-0000-4000-8000-000000000003", // user1
  "00000000-0000-4000-8000-000000000004", // user2
  "00000000-0000-4000-8000-000000000005", // user3
  "00000000-0000-4000-8000-000000000006", // user4
];

const BENEFITS = [
  "free_food", "discounted_food", "snap_accepted", "student_discount",
  "senior_discount", "kids_eat_free", "bogo", "coupon", "free_breakfast",
  "other", "military_discount", "everyone",
];

const NAME_SUFFIXES = [
  "Pantry", "Food Bank", "Market", "Kitchen", "Cafe", "Grocery Co-op",
  "Diner", "Community Center", "Food Hub", "Bakery",
];

// Rough lat/lon boxes per metro city — not real addresses, just plausible clustering.
const CITY_BOUNDS = {
  Portland:  { state: "OR", area: "503", lat: [45.43, 45.60], lon: [-122.82, -122.47], weight: 5 },
  Vancouver: { state: "WA", area: "360", lat: [45.60, 45.74], lon: [-122.75, -122.45], weight: 2 },
  Beaverton: { state: "OR", area: "503", lat: [45.45, 45.50], lon: [-122.85, -122.78], weight: 2 },
  Hillsboro: { state: "OR", area: "503", lat: [45.48, 45.56], lon: [-122.95, -122.87], weight: 2 },
  Aloha:     { state: "OR", area: "503", lat: [45.48, 45.51], lon: [-122.88, -122.84], weight: 1 },
  Tigard:    { state: "OR", area: "503", lat: [45.39, 45.44], lon: [-122.80, -122.75], weight: 1 },
  Gresham:   { state: "OR", area: "503", lat: [45.47, 45.52], lon: [-122.46, -122.38], weight: 2 },
};
const CITY_POOL = Object.entries(CITY_BOUNDS).flatMap(([city, c]) => Array(c.weight).fill(city));

const DAYS = ["monday", "tuesday", "wednesday", "thursday", "friday", "saturday", "sunday"];
const OPEN_TIMES = ["07:00", "08:00", "09:00", "10:00", "11:00"];
const CLOSE_TIMES = ["14:00", "15:00", "16:00", "17:00", "18:00", "19:00", "20:00"];

function sql(value) {
  if (value === null || value === undefined) return "NULL";
  if (typeof value === "boolean") return value ? "true" : "false";
  if (typeof value === "number") return String(value);
  return `'${String(value).replace(/'/g, "''")}'`;
}
function sqlArray(values, type) {
  if (!values || values.length === 0) return "NULL";
  return `ARRAY[${values.map((v) => sql(v)).join(",")}]::${type}[]`;
}
function pickStatus() {
  const r = faker.number.float({ min: 0, max: 1 });
  return r < 0.8 ? "verified" : r < 0.9 ? "pending" : "rejected";
}
function pastThenUpdated() {
  const createdAt = faker.date.past({ years: 2, refDate: REFERENCE_DATE });
  const updatedAt = faker.date.between({ from: createdAt, to: REFERENCE_DATE });
  return [createdAt.toISOString(), updatedAt.toISOString()];
}

// ── Resources ────────────────────────────────────────────────
const usedNames = new Set();
const resourceRows = [];
const resourceIds = [];

for (let i = 0; i < RESOURCE_COUNT; i++) {
  let name;
  do {
    name = `${faker.company.name()} ${faker.helpers.arrayElement(NAME_SUFFIXES)}`;
  } while (usedNames.has(name.toLowerCase()));
  usedNames.add(name.toLowerCase());

  const id = faker.string.uuid();
  resourceIds.push(id);

  const benefits = faker.helpers.arrayElements(BENEFITS, { min: 1, max: 3 });
  const [createdAt, updatedAt] = pastThenUpdated();

  resourceRows.push(`  (${sql(id)}, ${sql(name)}, ${sql(faker.lorem.sentences({ min: 1, max: 3 }))}, ` +
    `${sql(faker.lorem.sentence())}, ${faker.datatype.boolean(0.5) ? sql(faker.company.name() + " directory") : "NULL"}, ` +
    `${sqlArray(benefits, "benefit_category")}, ${sql(pickStatus())}, ` +
    `${faker.datatype.boolean(0.15) ? sql(faker.date.future({ refDate: REFERENCE_DATE }).toISOString().slice(0, 10)) : "NULL"}, ` +
    `${sql(faker.datatype.boolean(0.9))}, ${sql(faker.helpers.arrayElement(USER_IDS))}, ${sql(createdAt)}, ${sql(updatedAt)})`);
}

// ── Physical Locations ──────────────────────────────────────
const usedAddresses = new Set();
const locationRows = [];
const locationIds = [];

for (let i = 0; i < LOCATION_COUNT; i++) {
  const city = faker.helpers.arrayElement(CITY_POOL);
  const bounds = CITY_BOUNDS[city];

  let address, address2, addrKey;
  do {
    address = faker.location.streetAddress();
    address2 = faker.datatype.boolean(0.2) ? faker.location.secondaryAddress() : null;
    addrKey = `${address}|${address2 ?? ""}|${city}`.toLowerCase();
  } while (usedAddresses.has(addrKey));
  usedAddresses.add(addrKey);

  const id = faker.string.uuid();
  locationIds.push(id);

  const lat = faker.location.latitude({ min: bounds.lat[0], max: bounds.lat[1] });
  const lon = faker.location.longitude({ min: bounds.lon[0], max: bounds.lon[1] });
  const phone = faker.datatype.boolean(0.7)
    ? `(${bounds.area}) ${faker.string.numeric(3)}-${faker.string.numeric(4)}`
    : null;

  locationRows.push(`  (${sql(id)}, ${sql(faker.helpers.arrayElement(resourceIds))}, ${sql(address)}, ${sql(address2)}, ` +
    `${sql(city)}, ${sql(bounds.state)}, ${sql(faker.location.zipCode())}, ` +
    `${faker.datatype.boolean(0.5) ? sql(faker.location.county()) : "NULL"}, ${lat}, ${lon}, ${sql(phone)}, ` +
    `${sql(pickStatus())}, ${faker.datatype.boolean(0.4) ? sql(faker.lorem.sentence()) : "NULL"}, ` +
    `${sql(faker.helpers.arrayElement(USER_IDS))}, ${sql(faker.date.past({ years: 2, refDate: REFERENCE_DATE }).toISOString())})`);
}

// ── Resource Hours ───────────────────────────────────────────
const hoursRows = [];

for (const locationId of locationIds) {
  const days = faker.helpers.arrayElements(DAYS, { min: 2, max: 7 });
  const seasonal = faker.datatype.boolean(0.1);
  let validFrom = null, validUntil = null;
  if (seasonal) {
    const from = faker.date.soon({ days: 60, refDate: REFERENCE_DATE });
    validFrom = from.toISOString().slice(0, 10);
    validUntil = faker.date.soon({ days: 240, refDate: from }).toISOString().slice(0, 10);
  }
  for (const day of days) {
    const opens = faker.helpers.arrayElement(OPEN_TIMES);
    const closes = faker.helpers.arrayElement(CLOSE_TIMES);
    hoursRows.push(`  (${sql(locationId)}, ${sql(day)}, ${sql(opens)}, ${sql(closes)}, ` +
      `${faker.datatype.boolean(0.15) ? sql(faker.lorem.sentence()) : "NULL"}, ${sql(validFrom)}, ${sql(validUntil)})`);
  }
}

// ── Assemble ─────────────────────────────────────────────────
const block = `${BEGIN_MARKER}
-- ${RESOURCE_COUNT} resources, ${LOCATION_COUNT} locations, ${hoursRows.length} hours rows.
-- Resources are seeded first and fully, then locations are randomly assigned to
-- resources, then hours are randomly assigned to locations.

INSERT INTO resources (
  id, name, description, offer_desc, offer_source, benefits,
  verification_status, expires_at, is_active, created_by, created_at, updated_at
) VALUES
${resourceRows.join(",\n")};

INSERT INTO physical_locations (
  id, resource_id, address, address2, city, state, zip_code,
  neighborhood, latitude, longitude, phone_number, verification_status, notes,
  created_by, created_at
) VALUES
${locationRows.join(",\n")};

INSERT INTO resource_hours (
  physical_location_id, day, opens_at, closes_at, notes, valid_from, valid_until
) VALUES
${hoursRows.join(",\n")};
${END_MARKER}
`;

const current = readFileSync(SEED_PATH, "utf8");
const beginIdx = current.indexOf(BEGIN_MARKER);
const endIdx = current.indexOf(END_MARKER);
const withoutOldBlock = beginIdx !== -1 && endIdx !== -1
  ? current.slice(0, beginIdx) + current.slice(endIdx + END_MARKER.length + 1)
  : current;

writeFileSync(SEED_PATH, withoutOldBlock.trimEnd() + "\n\n" + block);
console.log(`Wrote ${resourceRows.length} resources, ${locationRows.length} locations, ${hoursRows.length} hours rows.`);
