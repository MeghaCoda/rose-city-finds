// Generates all non-auth seed data (businesses -> locations/offers ->
// offer_locations/location_hours/offer_hours/verification_events) using
// @faker-js/faker and writes it into supabase/seed.sql between markers.
// Re-run any time to regenerate; it replaces only its own marked block,
// leaving the hand-crafted auth user fixtures above it untouched.
//
// Deterministic: a fixed faker seed + a fixed reference "now" date mean every
// run produces byte-identical output. Bump SEED to intentionally get a new set.
//
// Usage: node scripts/generate-bulk-seed.mjs

import { faker } from "@faker-js/faker";
import { readFileSync, writeFileSync } from "node:fs";
import { sql, sqlArray } from "./lib/seedFormat.mjs";
import { pickVerificationStatus, pastThenUpdated, weightedCount, buildHoursRows, pickHoursNotes, buildVerificationEvent } from "./lib/seedGenerators.mjs";

const SEED = 20260630;
const REFERENCE_DATE = new Date("2026-06-30T00:00:00Z");

const SEED_PATH = new URL("../supabase/seed.sql", import.meta.url);
const BEGIN_MARKER = "-- BEGIN FAKER BULK SEED (generated by scripts/generate-bulk-seed.mjs)";
const END_MARKER = "-- END FAKER BULK SEED";

const BUSINESS_COUNT = 150;

// Reuse the users already created in the hand-crafted part of seed.sql.
const USER_IDS = [
  "00000000-0000-4000-8000-000000000001", // admin
  "00000000-0000-4000-8000-000000000002", // admin2
  "00000000-0000-4000-8000-000000000003", // user1
  "00000000-0000-4000-8000-000000000004", // user2
  "00000000-0000-4000-8000-000000000005", // user3
  "00000000-0000-4000-8000-000000000006", // user4
];
const ADMIN_IDS = USER_IDS.slice(0, 2);

const VENUE_TYPES = [
  "food_pantry", "food_bank", "restaurant", "cafe",
  "grocery_store", "farmers_market", "community_organization", "other",
];
const NAME_SUFFIXES_BY_VENUE_TYPE = {
  food_pantry: ["Pantry", "Community Pantry", "Food Pantry"],
  food_bank: ["Food Bank", "Regional Food Bank"],
  restaurant: ["Diner", "Kitchen", "Grill", "Bistro"],
  cafe: ["Cafe", "Coffee House", "Bakery"],
  grocery_store: ["Grocery Co-op", "Market", "Grocers"],
  farmers_market: ["Farmers Market", "Public Market"],
  community_organization: ["Community Center", "Community Hub", "Services Hub"],
  other: ["Food Hub", "Resource Center"],
};

const PRICE_TYPES = ["free", "discount"];
const ELIGIBILITY_TYPES = [
  "anyone", "student", "senior", "kids", "military", "snap", "income_requirement", "other",
];
const FOOD_FORMATS = ["dine_in", "grocery", "pickup", "delivery"];
const VERIFICATION_METHODS = ["phone", "document", "site_visit", "email"];

// Rough lat/lon boxes per metro city — not real addresses, just plausible clustering.
const CITY_BOUNDS = {
  Portland:  { state: "OR", area: "503", lat: [45.43, 45.60], lon: [-122.82, -122.47], weight: 5 },
  Vancouver: { state: "WA", area: "360", lat: [45.60, 45.74], lon: [-122.75, -122.45], weight: 2 },
  Beaverton: { state: "OR", area: "503", lat: [45.45, 45.50], lon: [-122.85, -122.78], weight: 2 },
  Hillsboro: { state: "OR", area: "503", lat: [45.48, 45.56], lon: [-122.95, -122.87], weight: 2 },
  Aloha:     { state: "OR", area: "503", lat: [45.48, 45.51], lon: [-122.88, -122.84], weight: 1 },
  Tigard:    { state: "OR", area: "503", lat: [45.39, 45.44], lon: [-122.80, -122.75], weight: 1 },
  Gresham:   { state: "OR", area: "503", lat: [45.47, 45.52], lon: [-122.46, -122.38], weight: 2 },
};
const CITY_POOL = Object.entries(CITY_BOUNDS).flatMap(([city, c]) => Array(c.weight).fill(city));

const DAYS = ["monday", "tuesday", "wednesday", "thursday", "friday", "saturday", "sunday"];
const OPEN_TIMES = ["07:00", "08:00", "09:00", "10:00", "11:00"];
const CLOSE_TIMES = ["14:00", "15:00", "16:00", "17:00", "18:00", "19:00", "20:00"];

const HOURS_CONFIG = { referenceDate: REFERENCE_DATE, days: DAYS, openTimes: OPEN_TIMES, closeTimes: CLOSE_TIMES };
const VERIFICATION_EVENT_CONFIG = { adminIds: ADMIN_IDS, verificationMethods: VERIFICATION_METHODS };

// Exported (in addition to the CLI guard below) so tests can call it
// directly without needing to fake process.argv/import.meta.url.
export function main() {
  faker.seed(SEED);

  // ── Businesses ───────────────────────────────────────────────
  const usedNames = new Set();
  const businesses = [];

  for (let i = 0; i < BUSINESS_COUNT; i++) {
    const venueType = faker.helpers.arrayElement(VENUE_TYPES);
    let name;
    do {
      name = `${faker.company.name()} ${faker.helpers.arrayElement(NAME_SUFFIXES_BY_VENUE_TYPE[venueType])}`;
    } while (usedNames.has(name.toLowerCase()));
    usedNames.add(name.toLowerCase());

    const [createdAt, updatedAt] = pastThenUpdated(REFERENCE_DATE);
    const verificationStatus = pickVerificationStatus();
    const statusChangedAt = verificationStatus === "pending"
      ? null
      : faker.date.between({ from: createdAt, to: updatedAt });

    businesses.push({
      id: faker.string.uuid(),
      name,
      venueType,
      verificationStatus,
      statusChangedAt,
      createdBy: faker.helpers.arrayElement(USER_IDS),
      createdAt,
      updatedAt,
      locationIds: [],
      offerIds: [],
    });
  }

  const businessRows = businesses.map((b) =>
    `  (${sql(b.id)}, ${sql(b.name)}, ${sql(faker.lorem.sentences({ min: 1, max: 3 }))}, ` +
    `${sql(b.venueType)}, ${sql(b.verificationStatus)}, ${sql(b.statusChangedAt?.toISOString())}, ` +
    `${faker.datatype.boolean(0.15) ? sql(faker.date.future({ refDate: REFERENCE_DATE }).toISOString().slice(0, 10)) : "NULL"}, ` +
    `${sql(faker.datatype.boolean(0.92))}, ${sql(b.createdBy)}, ${sql(b.createdAt.toISOString())}, ${sql(b.updatedAt.toISOString())}, ` +
    `${faker.datatype.boolean(0.3) ? sql(faker.lorem.sentence()) : "NULL"})`
  );

  // ── Locations ────────────────────────────────────────────────
  const usedAddresses = new Set();
  const locationRows = [];
  const allLocations = []; // flat list for hours generation

  for (const business of businesses) {
    const locationCount = weightedCount([[1, 0.65], [2, 0.25], [3, 0.10]]);

    for (let i = 0; i < locationCount; i++) {
      const city = faker.helpers.arrayElement(CITY_POOL);
      const bounds = CITY_BOUNDS[city];

      let address, address2, addrKey;
      do {
        address = faker.location.streetAddress();
        address2 = faker.datatype.boolean(0.2) ? faker.location.secondaryAddress() : null;
        addrKey = `${address}|${address2 ?? ""}|${city}`.toLowerCase();
      } while (usedAddresses.has(addrKey));
      usedAddresses.add(addrKey);

      const id = faker.string.uuid();
      business.locationIds.push(id);

      const lat = faker.location.latitude({ min: bounds.lat[0], max: bounds.lat[1] });
      const lon = faker.location.longitude({ min: bounds.lon[0], max: bounds.lon[1] });
      const phone = faker.datatype.boolean(0.7)
        ? `(${bounds.area}) ${faker.string.numeric(3)}-${faker.string.numeric(4)}`
        : null;
      const foodFormats = faker.helpers.arrayElements(FOOD_FORMATS, { min: 1, max: 3 });
      const verificationStatus = pickVerificationStatus();
      const createdAt = faker.date.past({ years: 2, refDate: REFERENCE_DATE });
      const statusChangedAt = verificationStatus === "pending"
        ? null
        : faker.date.between({ from: createdAt, to: REFERENCE_DATE });

      allLocations.push({ id, statusChangedAt });

      locationRows.push(`  (${sql(id)}, ${sql(business.id)}, ${sql(address)}, ${sql(address2)}, ` +
        `${sql(city)}, ${sql(bounds.state)}, ${sql(faker.location.zipCode())}, ` +
        `${faker.datatype.boolean(0.5) ? sql(faker.location.county()) : "NULL"}, ${lat}, ${lon}, ${sql(phone)}, ` +
        `${sqlArray(foodFormats, "public.food_format")}, ${sql(verificationStatus)}, ${sql(statusChangedAt?.toISOString())}, ` +
        `${sql(business.createdBy)}, ${sql(createdAt.toISOString())}, ` +
        `${faker.datatype.boolean(0.4) ? sql(faker.lorem.sentence()) : "NULL"}, ${sql(pickHoursNotes())})`);
    }
  }

  // ── Offers ───────────────────────────────────────────────────
  const offerRows = [];
  const offerLocationRows = [];
  const allOffers = []; // flat list for hours + verification_events generation

  for (const business of businesses) {
    const offerCount = weightedCount([[1, 0.55], [2, 0.30], [3, 0.15]]);

    for (let i = 0; i < offerCount; i++) {
      const eligibility = faker.helpers.arrayElements(ELIGIBILITY_TYPES, { min: 1, max: 2 });
      const isAnyone = eligibility.includes("anyone");
      const proofRequired = isAnyone ? false : faker.datatype.boolean(0.4);
      const priceType = faker.helpers.arrayElements(PRICE_TYPES, { min: 1, max: 2 });

      const isSeasonal = faker.datatype.boolean(0.1);
      let seasonStart = null, seasonEnd = null;
      if (isSeasonal) {
        const start = faker.date.soon({ days: 60, refDate: REFERENCE_DATE });
        seasonStart = start.toISOString().slice(0, 10);
        seasonEnd = faker.date.soon({ days: 240, refDate: start }).toISOString().slice(0, 10);
      }

      const [createdAt, updatedAt] = pastThenUpdated(REFERENCE_DATE);
      const verificationStatus = pickVerificationStatus();
      const statusChangedAt = verificationStatus === "pending"
        ? null
        : faker.date.between({ from: createdAt, to: updatedAt });

      const id = faker.string.uuid();
      business.offerIds.push(id);
      // Most offers rely on their location's hours; only a fraction set their own.
      const hasOwnHours = faker.datatype.boolean(0.2);
      allOffers.push({ id, statusChangedAt, hasOwnHours });

      offerRows.push(`  (${sql(id)}, ${sql(business.id)}, ${sql(`${faker.helpers.arrayElement(["Free", "Discounted", "Reduced-price"])} ${faker.commerce.productName()}`)}, ` +
        `${sql(faker.lorem.sentence())}, ${sqlArray(priceType, "public.price_type")}, ${sqlArray(eligibility, "public.eligibility_type")}, ` +
        `${sql(proofRequired)}, ${proofRequired ? sql(faker.lorem.sentence()) : "NULL"}, ` +
        `${faker.datatype.boolean(0.15) ? sql(faker.date.future({ refDate: REFERENCE_DATE }).toISOString().slice(0, 10)) : "NULL"}, ` +
        `${sql(isSeasonal)}, ${sql(seasonStart)}, ${sql(seasonEnd)}, ${sql(faker.datatype.boolean(0.9))}, ` +
        `${sql(verificationStatus)}, ${sql(statusChangedAt?.toISOString())}, ` +
        `${faker.datatype.boolean(0.15) ? sql(faker.date.future({ refDate: REFERENCE_DATE }).toISOString().slice(0, 10)) : "NULL"}, ` +
        `${sql(business.createdBy)}, ${sql(createdAt.toISOString())}, ${sql(updatedAt.toISOString())}, ` +
        `${faker.datatype.boolean(0.3) ? sql(faker.lorem.sentence()) : "NULL"}, ` +
        `${sql(hasOwnHours ? pickHoursNotes() : null)})`);

      // Link this offer to a subset of its business's locations (all of them, most of the time).
      const linkedLocations = faker.datatype.boolean(0.7)
        ? business.locationIds
        : faker.helpers.arrayElements(business.locationIds, { min: 1, max: business.locationIds.length });
      for (const locationId of linkedLocations) {
        offerLocationRows.push(`  (${sql(id)}, ${sql(locationId)})`);
      }
    }
  }

  // ── Location & Offer Hours ──────────────────────────────────
  const locationHoursRows = allLocations.flatMap((l) => buildHoursRows(l.id, HOURS_CONFIG));
  const offerHoursRows = allOffers
    .filter((o) => o.hasOwnHours)
    .flatMap((o) => buildHoursRows(o.id, HOURS_CONFIG));

  // ── Verification Events ─────────────────────────────────────
  const verificationEventRows = [
    ...businesses
      .filter((b) => b.statusChangedAt)
      .map((b) => buildVerificationEvent("business_id", b.id, { at: b.statusChangedAt, outcome: b.verificationStatus }, VERIFICATION_EVENT_CONFIG)),
    ...allLocations
      .filter((l) => l.statusChangedAt)
      .map((l) => buildVerificationEvent("location_id", l.id, { at: l.statusChangedAt, outcome: "verified" }, VERIFICATION_EVENT_CONFIG)),
    ...allOffers
      .filter((o) => o.statusChangedAt)
      .map((o) => buildVerificationEvent("offer_id", o.id, { at: o.statusChangedAt, outcome: "verified" }, VERIFICATION_EVENT_CONFIG)),
  ];

  // ── Assemble ─────────────────────────────────────────────────
  const block = `${BEGIN_MARKER}
-- ${businesses.length} businesses, ${locationRows.length} locations, ${offerRows.length} offers,
-- ${offerLocationRows.length} offer_locations, ${locationHoursRows.length} location_hours rows,
-- ${offerHoursRows.length} offer_hours rows, ${verificationEventRows.length} verification_events.
-- No hand-picked edge cases here by design -- this block is pure random
-- filler for volume/pagination/map-density testing. Deliberate edge
-- cases (specific status/nullability combos) should be added by hand
-- above this block, once we've settled on what the new set should cover.

INSERT INTO public.businesses (
  id, name, description, venue_type, verification_status, verification_status_changed_at,
  verification_expires_at, is_active, created_by, created_at, updated_at, notes
) VALUES
${businessRows.join(",\n")};

INSERT INTO public.locations (
  id, business_id, address, address2, city, state, zip_code,
  neighborhood, latitude, longitude, phone_number, food_formats,
  verification_status, verification_status_changed_at, created_by, created_at, notes, hours_notes
) VALUES
${locationRows.join(",\n")};

INSERT INTO public.offers (
  id, business_id, name, description, price_type, eligibility,
  proof_required, proof_desc, expires_at, is_seasonal, season_start_date, season_end_date,
  is_active, verification_status, verification_status_changed_at, verification_expires_at,
  created_by, created_at, updated_at, notes, hours_notes
) VALUES
${offerRows.join(",\n")};

INSERT INTO public.offer_locations (offer_id, location_id) VALUES
${offerLocationRows.join(",\n")};

INSERT INTO public.location_hours (
  location_id, day, opens_at, closes_at, valid_from, valid_until
) VALUES
${locationHoursRows.join(",\n")};

INSERT INTO public.offer_hours (
  offer_id, day, opens_at, closes_at, valid_from, valid_until
) VALUES
${offerHoursRows.join(",\n")};

INSERT INTO public.verification_events (
  verified_at, verified_by, method, outcome, notes, business_id, offer_id, location_id
) VALUES
${verificationEventRows.join(",\n")};
${END_MARKER}
`;

  const current = readFileSync(SEED_PATH, "utf8");
  const beginIdx = current.indexOf(BEGIN_MARKER);
  const endIdx = current.indexOf(END_MARKER);
  const withoutOldBlock = beginIdx !== -1 && endIdx !== -1
    ? current.slice(0, beginIdx) + current.slice(endIdx + END_MARKER.length + 1)
    : current;

  writeFileSync(SEED_PATH, withoutOldBlock.trimEnd() + "\n\n" + block);
  console.log(`Wrote ${businessRows.length} businesses, ${locationRows.length} locations, ` +
    `${offerRows.length} offers, ${offerLocationRows.length} offer_locations, ` +
    `${locationHoursRows.length} location_hours, ${offerHoursRows.length} offer_hours, ` +
    `${verificationEventRows.length} verification_events.`);
}

// Only run the generator (and its file-write) when this script is executed
// directly (`node scripts/generate-bulk-seed.mjs`) — not when its helpers
// are imported elsewhere, e.g. by unit tests.
if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
